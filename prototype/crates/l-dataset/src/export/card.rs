//! Dataset card + known limits (atom #397 · E.3.11).
//!
//! # Madness
//!
//! A dataset is not honest until its card is. [`DatasetCard`] states the
//! *sources* and their counts, the *excluded* data (private user memory, live
//! secrets), the *privacy gates* (PII zero, encoded-secret reject), the *reward
//! rules* (S1 ground-truth only, hard-nullifier-first), the *split leakage
//! guard*, the *known biases / limits*, and the *GRPO locked* status. The card
//! renders deterministically to markdown, and its `sha256` is what the Stage G
//! unlock packet pins as `dataset_card_hash` — so a silently edited card breaks
//! the unlock.
use crate::hex32_encode;

/// Per-source sample counts as declared by the card.
#[derive(Clone, Copy, Debug, Eq, PartialEq, Hash)]
pub struct SourceCounts {
    /// S1 ground-truth (repair/measure-backed) sample count.
    pub s1_ground_truth_u64: u64,
    /// S2 narrative (no-reward) sample count.
    pub s2_narrative_u64: u64,
    /// Security corpus (audit/exploit-repro) sample count.
    pub security_u64: u64,
    /// Human PR/review sample count.
    pub human_review_u64: u64,
    /// MURPHY repair-tree sample count.
    pub murphy_repair_u64: u64,
    /// Didactic signal sample count.
    pub didactic_u64: u64,
}

impl SourceCounts {
    /// Total sample count across all sources.
    pub const fn total(&self) -> u64 {
        self.s1_ground_truth_u64
            .saturating_add(self.s2_narrative_u64)
            .saturating_add(self.security_u64)
            .saturating_add(self.human_review_u64)
            .saturating_add(self.murphy_repair_u64)
            .saturating_add(self.didactic_u64)
    }
}

/// A dataset card: the human- and machine-readable statement of what the dataset
/// is, what it excludes, and what rules govern it.
#[derive(Clone, Debug, Eq, PartialEq)]
pub struct DatasetCard {
    /// Dataset version tag (e.g. `stage_e_v0`).
    pub dataset_version: String,
    /// Per-source sample counts.
    pub source_counts: SourceCounts,
    /// Whether the dataset passed PII-zero.
    pub pii_zero: bool,
    /// Whether private user memory is excluded from training.
    pub user_memory_excluded: bool,
    /// One-line statement of the reward policy.
    pub reward_policy: String,
    /// Whether the split leakage guard is enforced.
    pub split_leakage_guarded: bool,
    /// Whether GRPO export remains locked (always `true` in Stage E).
    pub grpo_locked: bool,
    /// Known biases the dataset carries.
    pub known_biases: Vec<String>,
    /// Known limits / caveats.
    pub known_limits: Vec<String>,
}

impl DatasetCard {
    /// Render the card to deterministic markdown.
    pub fn render_markdown(&self) -> String {
        let c = &self.source_counts;
        let mut s = String::with_capacity(2048);
        s.push_str(&format!(
            "# MNEMOS Stage E Dataset Card — `{}`\n\n",
            self.dataset_version
        ));
        s.push_str("> Generated by `mnemos-l-dataset` export layer (atom #397 · E.3.11). ");
        s.push_str("Dataset-build-only: no model training ran.\n\n");

        s.push_str("## Source counts\n\n");
        s.push_str("| source | count |\n|---|---|\n");
        s.push_str(&format!(
            "| S1 ground-truth | {} |\n",
            c.s1_ground_truth_u64
        ));
        s.push_str(&format!(
            "| S2 narrative (no reward) | {} |\n",
            c.s2_narrative_u64
        ));
        s.push_str(&format!("| security corpus | {} |\n", c.security_u64));
        s.push_str(&format!("| human PR/review | {} |\n", c.human_review_u64));
        s.push_str(&format!(
            "| MURPHY repair-tree | {} |\n",
            c.murphy_repair_u64
        ));
        s.push_str(&format!("| didactic signal | {} |\n", c.didactic_u64));
        s.push_str(&format!("| **total** | **{}** |\n\n", c.total()));

        s.push_str("## Excluded data\n\n");
        s.push_str(&format!(
            "- Private user memory excluded from training: **{}**\n",
            self.user_memory_excluded
        ));
        s.push_str("- Live secrets / wallet keys / sponsor keys / provider response bodies: **excluded (hard reject)**\n");
        s.push_str(
            "- Encoded secrets (base64 / hex / gzip / zip entropy spikes): **rejected**\n\n",
        );

        s.push_str("## Privacy gates\n\n");
        s.push_str(&format!("- PII zero: **{}**\n", self.pii_zero));
        s.push_str(
            "- Every export candidate scanned by the canonical privacy scanner before write.\n",
        );
        s.push_str("- Errors are byte-free (no raw record echoed into diagnostics).\n\n");

        s.push_str("## Reward rules\n\n");
        s.push_str(&format!("- {}\n", self.reward_policy));
        s.push_str("- S2 narrative / self-report / human praise can enter SFT context but **never set reward**.\n");
        s.push_str("- Hard nullifiers fire before any positive label; NAITE composite weights = `[32,20,16,12,10,6,4]` (sum 100).\n\n");

        s.push_str("## Split leakage guard\n\n");
        s.push_str(&format!(
            "- Split leakage guard enforced: **{}**\n",
            self.split_leakage_guarded
        ));
        s.push_str("- Same task / issue / diff lineage / MURPHY tree co-locate on one split side; held-out is isolated.\n\n");

        s.push_str("## Known biases\n\n");
        if self.known_biases.is_empty() {
            s.push_str("- (none declared)\n");
        } else {
            for b in &self.known_biases {
                s.push_str(&format!("- {b}\n"));
            }
        }
        s.push('\n');

        s.push_str("## Known limits\n\n");
        if self.known_limits.is_empty() {
            s.push_str("- (none declared)\n");
        } else {
            for l in &self.known_limits {
                s.push_str(&format!("- {l}\n"));
            }
        }
        s.push('\n');

        s.push_str("## GRPO / unlock status\n\n");
        s.push_str(&format!("- GRPO export locked: **{}**\n", self.grpo_locked));
        s.push_str("- `StageGUnlockPacket.sft_smoke_ready` flips only when PII 0, split, shard hashes, this card, the evidence bundle, and the didactic/self-evolution schema gates are all green.\n");
        s
    }

    /// The `sha256` of the rendered markdown — the card's content address.
    pub fn card_hash(&self) -> [u8; 32] {
        crate::sha256(self.render_markdown().as_bytes())
    }

    /// The lower-hex of [`card_hash`](DatasetCard::card_hash).
    pub fn card_hash_hex(&self) -> String {
        hex32_encode(&self.card_hash())
    }
}

/// The canonical Stage E v0 dataset card. Conservative: it declares the export /
/// governance layer verified on fixtures with **0 training samples emitted** —
/// corpus emission and `sft_smoke_ready` are gated to the Stage G SFT smoke.
pub fn stage_e_v0() -> DatasetCard {
    DatasetCard {
        dataset_version: "stage_e_v0".to_string(),
        source_counts: SourceCounts {
            s1_ground_truth_u64: 0,
            s2_narrative_u64: 0,
            security_u64: 0,
            human_review_u64: 0,
            murphy_repair_u64: 0,
            didactic_u64: 0,
        },
        pii_zero: true,
        user_memory_excluded: true,
        reward_policy: "Reward is granted only to S1 ground-truth records that pass an S1 reverify; everything else is no-reward.".to_string(),
        split_leakage_guarded: true,
        grpo_locked: true,
        known_biases: vec![
            "Corpus is drawn from a single project's A-D build trace (MNEMOS); domain transfer is unproven.".to_string(),
            "Bilingual (Korean + English) rationale skews toward this project's two-language workflow.".to_string(),
            "Verification-heavy trace over-represents gate/clippy/test repair relative to greenfield design.".to_string(),
        ],
        known_limits: vec![
            "Stage E emits export tooling + governance only; 0 training samples are written to disk (corpus emission is gated to Stage G).".to_string(),
            "`sft_smoke_ready` is false until the full corpus, evidence bundle, and didactic/self-evolution schema gates are materialized and green.".to_string(),
            "GRPO export stays locked and self-evolution promotion stays locked throughout Stage E.".to_string(),
            "Sample counts are 0 by construction here; they are populated when the corpus is emitted under the Stage G unlock.".to_string(),
        ],
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn card_includes_source_counts() {
        let md = stage_e_v0().render_markdown();
        assert!(md.contains("## Source counts"));
        assert!(md.contains("S1 ground-truth"));
        assert!(md.contains("| **total** |"));
    }

    #[test]
    fn card_states_pii_zero() {
        let md = stage_e_v0().render_markdown();
        assert!(md.contains("PII zero: **true**"));
    }

    #[test]
    fn card_states_user_memory_excluded_and_no_training() {
        let md = stage_e_v0().render_markdown();
        assert!(md.contains("Private user memory excluded from training: **true**"));
        assert!(md.contains("no model training ran"));
    }

    #[test]
    fn card_states_reward_policy_s1_only() {
        let md = stage_e_v0().render_markdown();
        assert!(md.contains("## Reward rules"));
        assert!(md.contains("S1 ground-truth"));
        assert!(md.contains("never set reward"));
    }

    #[test]
    fn card_lists_known_limits_and_split_guard_and_grpo_locked() {
        let md = stage_e_v0().render_markdown();
        assert!(md.contains("## Known limits"));
        assert!(md.contains("Split leakage guard enforced: **true**"));
        assert!(md.contains("GRPO export locked: **true**"));
    }

    #[test]
    fn card_hash_is_stable_and_matches_render() {
        let card = stage_e_v0();
        assert_eq!(
            card.card_hash(),
            crate::sha256(card.render_markdown().as_bytes())
        );
        // re-rendering is deterministic ⇒ stable hash.
        assert_eq!(card.card_hash(), stage_e_v0().card_hash());
        assert_eq!(card.card_hash_hex().len(), 64);
    }

    #[test]
    fn total_counts_sum() {
        let c = SourceCounts {
            s1_ground_truth_u64: 2,
            s2_narrative_u64: 3,
            security_u64: 1,
            human_review_u64: 4,
            murphy_repair_u64: 5,
            didactic_u64: 6,
        };
        assert_eq!(c.total(), 21);
    }

    #[test]
    fn on_disk_card_matches_render() {
        // The committed datasets/stage_e/DATASET_CARD.md must be generated from
        // this exact render — compile-time bound, so a hand-edit breaks the test.
        assert_eq!(
            stage_e_v0().render_markdown(),
            include_str!("../../../../../datasets/stage_e/DATASET_CARD.md")
        );
    }
}
